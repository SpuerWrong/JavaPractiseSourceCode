package OOP;

/**
 * @author 王丽杰
 * @date 2024/10/24
 * @className StudentEncapsulation
 * @package OOP
 * @description 封装
 */
public class StudentEncapsulation {
    private String name;
    private int id;
    private char sex;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public char getSex() {
        return sex;
    }

    public void setSex(char sex) {
        this.sex = sex;
    }
}
